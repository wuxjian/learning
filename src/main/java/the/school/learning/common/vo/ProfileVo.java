package the.school.learning.common.vo;

public class ProfileVo {
    private Integer learnCount;
    private Integer learnTimes;
    private Integer commentCount;
    private Integer commentReplyCount;

    public Integer getLearnCount() {
        return learnCount;
    }

    public void setLearnCount(Integer learnCount) {
        this.learnCount = learnCount;
    }

    public Integer getLearnTimes() {
        return learnTimes;
    }

    public void setLearnTimes(Integer learnTimes) {
        this.learnTimes = learnTimes;
    }

    public Integer getCommentCount() {
        return commentCount;
    }

    public void setCommentCount(Integer commentCount) {
        this.commentCount = commentCount;
    }

    public Integer getCommentReplyCount() {
        return commentReplyCount;
    }

    public void setCommentReplyCount(Integer commentReplyCount) {
        this.commentReplyCount = commentReplyCount;
    }
}
